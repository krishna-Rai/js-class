let day = 1
let dayName;

switch(day){
    case 1: 
        dayName = 'Sunday'

    case 2:
        dayName = 'Monday'

    case 3:
        dayName = 'Tuesday'
        
    case 4:
        dayName = 'Wed'
        
    case 5:
        dayName = 'Thu'
        
    case 6:
        dayName = 'Fri'
        
    case 7:
        dayName = 'Sat'
        
    default:
        dayName = 'Invalid day'

}
console.log(dayName)
